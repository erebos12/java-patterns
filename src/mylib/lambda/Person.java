package mylib.lambda;

public class Person {

	private int age;
	private String surname;
	private String lastname;

	public enum Gender {
		MALE, FEMALE
	};

	public Person() {

	}

	public int getAge() {
		return age;
	}

	public String getSurname() {
		return surname;
	}

	public String getLastname() {
		return lastname;
	}
	
	public String getGender() {
		return lastname;
	}


}
